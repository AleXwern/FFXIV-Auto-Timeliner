#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*str;
	size_t			i;

	i = 0;
	str = (unsigned char *)b;
	if (str == NULL)
		return (NULL);
	while (len > i)
	{
		str[i] = (unsigned char)c;
		i++;
	}
	return (str);
}
